CREATE TABLE CERTIFICATE (
	ID NUMBER(*) PRIMARY KEY,
	RECEIVE_DATE DATE,
	COMPANY NVARCHAR2(90),
	NAME NVARCHAR2(90),
	CERT_NUMBER NUMBER(*),
	SCAN_ID NUMBER NOT NULL
);

CREATE TABLE SCAN (
	ID NUMBER(*) PRIMARY KEY,
	SCAN BLOB NOT NULL
);

CREATE TABLE GRADE (
	ID NUMBER(*) PRIMARY KEY,
	NAME NVARCHAR2(90)
);

CREATE TABLE POST (
	ID NUMBER(*) PRIMARY KEY,
	NAME NVARCHAR2(90)
);

CREATE TABLE DEPARTMENT (
	ID NUMBER(*) PRIMARY KEY,
	NAME NVARCHAR2(90),
	HEAD_EMPLOYEE_ID NUMBER(*)
);

CREATE TABLE EMPLOYEE (
	ID NUMBER(*) PRIMARY KEY,
	NAME NVARCHAR2(90),
	SURNAME NVARCHAR2(90),
	FATHER_NAME NVARCHAR2(90),
	BIRTH_DATE DATE,
	SALARY NUMBER(*),
    POST_ID NUMBER(*),
	GRADE_ID NUMBER(*),
    DEPARTMENT_ID NUMBER(*),
	CERTIFICATE_ID NUMBER(*)
);

CREATE TABLE DEP_HAS_PARENT_DEP (
	DEPARTMENT_ID NUMBER(*),
	PARENT_DEPARTMENT_ID NUMBER(*)
);

